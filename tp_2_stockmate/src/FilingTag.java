
public class FilingTag {
	
	private String tag;
	private String fullDesc;
	
	public FilingTag(String tag, String fullDesc) {
		this.tag = tag;
		this.fullDesc = fullDesc;
	}
	
	public String getTag() {
		return this.tag;
	}
	
	public String getFullDescription() {
		return this.fullDesc;
	}
	
}
